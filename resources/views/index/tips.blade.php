									<p>Pool water should be tested regularly to make sure it's clean and healthy. The pH scale is a measurement of acidity or alkalinity that runs from 0 to 14. A reading between 7.2 and 7.8 is ideal; this range is safe for swimmers and helps sanitizers work at top efficiency.</p>

									<p>You can monitor your pool's pH level with a testing kit. There are many kinds of testing kits available; however, most homeowner versions are either reagent kits or test-strips. Reagent kits aren't too difficult to use. You take a sample of pool water, then add liquids or tablets to it. The water changes color, indicating its chemical balance. Test-strips work differently. When you submerge them in the pool for a few seconds, dyes they contain cause them to change color. Next, match up the strip to a color chart to determine the pool's pH level. Use this information to gauge what kind and how much of the chemicals your pool needs.</p>
