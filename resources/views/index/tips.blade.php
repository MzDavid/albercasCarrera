									<p>Sometimes it's difficult to determine if low water levels are due to evaporation or a leak. You can discover leaks in your pool by conducting a simple bucket test. Fill a plastic bucket three-quarters full of water. On the inside of the bucket, mark the water line. Place the bucket in the pool, then mark the water line on the outside of the container. (If the bucket has a handle, remove it to allow for better stability while floating.) Let it float for two or three days. If the water inside and outside the bucket has gone down the same amount, your pool is losing water due to evaporation. However, if the pool water level has gone down more than the water inside the bucket, your pool has a leak. That's your cue to call a professional to have it patched.
